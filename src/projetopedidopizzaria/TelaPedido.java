    private void imprimePedido(){
        try(PrintStream ps = new PrintStream(new File("Pedido.txt"))){
            ps.println(pedido);
            JOptionPane.showMessageDialog(this, "Pedido impresso com sucesso!",getTitle(),JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Houve um erro ao tentar o imprimir o pedido: \n"+e.getMessage(),getTitle(),JOptionPane.ERROR_MESSAGE);
        }
        
    }
    private void btAddPedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddPedActionPerformed
        
        pedido.addItem(criaItem());

        modeloTabela.getDataVector().clear();
         for (Item it : pedido.getListaItens()){
             modeloTabela.addRow(new Object[] {it.getSabor(),it.getTamanho(),it.getQuantidade(),it.getValor()});            
        }
         limpaPizza();
         tfSubtotalPedido.setText(String.valueOf(calculaSubtotalPedido()));
    }//GEN-LAST:event_btAddPedActionPerformed

    private void fecharPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fecharPedidoActionPerformed
         
        pedido.setCliente(tfNomeCliente.getText());
        pedido.setEndereco(tfEndereco.getText());
        pedido.setTelefone(tfTel.getText());
        pedido.setCidade(cbxCidade.getSelectedItem().toString());
        imprimePedido();
        limpaPedido();
        
        pedido = new Pedido();
            

    }//GEN-LAST:event_fecharPedidoActionPerformed

