    public Item criaItem(){
        Item it = new Item();
        
        it.setSabor(rbtgSabor.getSelection().getActionCommand());
        it.setValor(calculaSubtotalPizza());
        it.setQuantidade(Integer.parseInt(tfUnidades.getText()));
        it.setTamanho(rbtgTamanho.getSelection().getActionCommand());
        
        if (cbMassaIntegral.isSelected()) {
            it.setMassaIntegral("Massa integral");
        } else if (cbBordaCheddar.isSelected()) {
            it.setBordaChedar("Borda cheddar");
        } else if (cbBordaChoc.isSelected()) {
            it.setBordaChocolate("Borda chocolate");
        } else if (cbBordaCat.isSelected()) {
            it.setOutro("Borda Catupiry");
        }
        
        return it;
        
    }

    public float calculaSubtotalPizza() {
       return (valorTamanho+cbMassa+cbCheddar+cbChoc+cbOutros) * Integer.parseInt(tfUnidades.getText());
        
    }
    public float calculaSubtotalPedido(){
        float valor = 0.0f;
        for(Item it : pedido.getListaItens()){
            valor += it.getValor();
        }
        return valor;
    }
    

